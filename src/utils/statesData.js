// Helper function to get states for a country
export const getStatesForCountry = (countryCode) => {
  const country = COUNTRIES.find(c => c.value === countryCode);
  return country?.states || [];
};

// Helper function to check if country has states
export const countryHasStates = (countryCode) => {
  const country = COUNTRIES.find(c => c.value === countryCode);
  return country?.hasStates || false;
};

// Helper function to get state label for country
export const getStateLabelForCountry = (countryCode) => {
  const country = COUNTRIES.find(c => c.value === countryCode);
  return country?.stateLabel || 'Region/State';
};

// Helper function to get regions for a country's states
export const getRegionsForCountry = (countryCode) => {
  const states = getStatesForCountry(countryCode);
  const regions = [...new Set(states.map(state => state.region))];
  return regions.sort();
};
