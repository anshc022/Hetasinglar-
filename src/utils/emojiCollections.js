export const filterEmojiGroups = (groups, query) => {
  const normalizedQuery = query?.trim();
  if (!normalizedQuery) {
    return Object.entries(groups);
  }

  const matches = [];

  for (const [groupKey, emojis] of Object.entries(groups)) {
    const filtered = emojis.filter((emoji) => emoji.includes(normalizedQuery));
    if (filtered.length > 0) {
      matches.push([groupKey, filtered]);
    }
  }

  return matches;
};
